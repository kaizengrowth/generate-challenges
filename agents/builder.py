# ── Planner ──────────────────────────────────────────────────────────────────

def plan_challenges(
    challenge_descriptions: list[str],
    topic: str = "",
    instructor_notes: str = "",
    existing_repos: list[dict] | None = None,
) -> BuildPlan:
    """
    Lightweight LLM call: decide clustering, ecosystem, file list per repo.

    Args:
        challenge_descriptions: challenges to generate (may include existing ones in amend mode)
        topic: broader topic for knowledge base lookup
        instructor_notes: any guidance from the instructor
        existing_repos: when amending, info about existing repos
                        [{"name": ..., "ecosystem": ..., "challenges": [...]}]
    """
    kb = _load_knowledge_base(topic)

    parts = ["## Challenges to Plan\n"]
    for i, desc in enumerate(challenge_descriptions, 1):
        parts.append(f"{i}. {desc}")

    if instructor_notes:
        parts.append(f"\n## Instructor Notes\n{instructor_notes}")

    if existing_repos:
        parts.append("\n## Existing Repos (amendment context)")
        parts.append("These repos already exist. Preserve their names and structure where "
                      "possible. Add new challenges to existing repos when they fit. Create "
                      "new repos only for challenges that don't fit existing ones.")
        for repo in existing_repos:
            challenges_str = ", ".join(repo.get("challenges", []))
            parts.append(f"- {repo['name']} (ecosystem: {repo['ecosystem']}"
                         f"{', challenges: ' + challenges_str if challenges_str else ''})")

    if kb:
        parts.append(f"\n## Knowledge Base (reuse these patterns)\n{kb}")

    raw = call_llm(
        system=PLANNER_SYSTEM_PROMPT,
        user="\n".join(parts),
        model=config.PLANNER_MODEL,
        max_tokens=config.PLANNER_MAX_TOKENS,
        agent="Builder (Planner)",
    )

    data = parse_json_from_response(raw, context="Builder (Planner)")

    repos = []
    for r in data["repos"]:
        repos.append(RepoSpec(
            name=r["name"],
            description=r["description"],
            challenges=r["challenges"],
            ecosystem=r["ecosystem"],
            install_command=r["install_command"],
            test_command=r["test_command"],
            creative_files=r["creative_files"],
            is_ui=r.get("is_ui", False),
        ))

    return BuildPlan(
        repos=repos,
        clustering_rationale=data.get("clustering_rationale", ""),
        challenge_type_notes=data.get("challenge_type_notes", ""),
    )


# ── Per-repo generator ──────────────────────────────────────────────────────

def generate_repo(
    spec: RepoSpec,
    topic: str = "",
    instructor_notes: str = "",
    revision_feedback: str = "",
    prior_files: dict[str, str] | None = None,
) -> ChallengeRepo:
    """Generate creative files for a single repo, using filtered reference docs."""
    ref_docs = _load_filtered_reference_docs(spec.ecosystem, spec.is_ui)
    kb = _load_knowledge_base(topic)

    parts = [
        "## Repo Plan\n"
        f"Name: {spec.name}\n"
        f"Description: {spec.description}\n"
        f"Ecosystem: {spec.ecosystem}\n"
        f"Challenges: {', '.join(spec.challenges)}\n"
        f"Files to generate: {', '.join(spec.creative_files)}"
    ]

    if instructor_notes:
        parts.append(f"\n## Instructor Notes\n{instructor_notes}")

    if kb:
        parts.append(f"\n## Knowledge Base (reuse these patterns)\n{kb}")

    parts.append(f"\n## Reference Documentation\n{ref_docs}")

    if revision_feedback:
        parts.append(
            f"\n## Revision Required\nStudent agents reviewed your previous attempt and found issues. "
            f"Please fix them:\n\n{revision_feedback}"
        )

    if prior_files:
        parts.append("\n## Your Previous Output (for revision context)")
        parts.append(format_files_for_prompt(prior_files))

    raw = call_llm(
        system=REPO_GENERATOR_SYSTEM_PROMPT,
        user="\n".join(parts),
        model=config.BUILDER_MODEL,
        max_tokens=config.BUILDER_MAX_TOKENS,
        agent="Builder",
    )

    data = parse_json_from_response(raw, context=f"Builder (repo: {spec.name})")

    # Merge boilerplate config files with LLM-generated creative files
    boilerplate = get_boilerplate(spec.ecosystem)
    all_files = {**boilerplate, **data["files"]}  # LLM files override if collision

    return ChallengeRepo(
        name=spec.name,
        description=spec.description,
        challenges=spec.challenges,
        install_command=spec.install_command,
        test_command=spec.test_command,
        files=all_files,
        ecosystem=spec.ecosystem,
    )


# ── Targeted reviser (refinement iterations only) ────────────────────────────

def _revise_repo(
    spec: RepoSpec,
    topic: str,
    instructor_notes: str,
    revision_feedback: str,
    prior_creative: dict[str, str],
) -> ChallengeRepo:
    """
    Targeted revision pass: send only prior creative files + feedback.

    Skips reference docs entirely (prior files already demonstrate the patterns).
    Outputs only the files that need to change; merges result with prior files.
    """
    parts = [
        "## Repo to Fix\n"
        f"Name: {spec.name}\n"
        f"Description: {spec.description}\n"
        f"Ecosystem: {spec.ecosystem}\n"
        f"Challenges: {', '.join(spec.challenges)}"
    ]

    if instructor_notes:
        parts.append(f"\n## Instructor Notes\n{instructor_notes}")

    parts.append(f"\n## Feedback to Address\n{revision_feedback}")

    parts.append("\n## Current Repo Files (fix these)")
    parts.append(format_files_for_prompt(prior_creative))

    raw = call_llm(
        system=REVISE_SYSTEM_PROMPT,
        user="\n".join(parts),
        model=config.BUILDER_MODEL,
        max_tokens=config.BUILDER_MAX_TOKENS,
        agent="Builder",
    )

    data = parse_json_from_response(raw, context=f"Builder revision (repo: {spec.name})")

    # Support both {"changed_files": ...} and fallback {"files": ...} if model slips
    changed_files = data.get("changed_files", data.get("files", {}))

    # Merge: boilerplate < prior creative < changed files (only changed files override)
    boilerplate = get_boilerplate(spec.ecosystem)
    all_files = {**boilerplate, **prior_creative, **changed_files}

    return ChallengeRepo(
        name=spec.name,
        description=spec.description,
        challenges=spec.challenges,
        install_command=spec.install_command,
        test_command=spec.test_command,
        files=all_files,
        ecosystem=spec.ecosystem,
    )


# ── Ecosystem detection (for revision/amendment without re-planning) ─────────

def _detect_ecosystem(files: dict[str, str]) -> str:
    """Infer ecosystem from existing files."""
    filenames = set(files.keys())
    if any(f.endswith(".vue") for f in filenames):
        return "vue"
    if "angular.json" in filenames:
        return "angular"
    if "pom.xml" in filenames:
        return "java"
    if "CMakeLists.txt" in filenames:
        return "cpp"
    if any(f.endswith(".cbl") for f in filenames):
        return "cobol"
    if "requirements.txt" in filenames:
        return "python"
    if any(f.endswith(".tsx") or f.endswith(".jsx") for f in filenames):
        return "react"
    return "typescript"


def _detect_is_ui(ecosystem: str) -> bool:
    return ecosystem in ("react", "vue", "angular")


def _infer_commands(ecosystem: str) -> tuple[str, str]:
    """Return (install_command, test_command) for an ecosystem."""
    commands = {
        "react":      ("npm install", "npm test"),
        "typescript": ("npm install", "npm test"),
        "vue":        ("npm install", "npm test"),
        "angular":    ("npm install", "npm test"),
        "python":     ("pip install -r requirements.txt", "pytest -x"),
        "java":       ("", "mvn test"),
        "cpp":        ("cmake -S . -B build && cmake --build build",
                       "cd build && ctest --stop-on-failure --output-on-failure"),
        "cobol":      ("", "bash tests/run_tests.sh"),
    }
    return commands.get(ecosystem, ("npm install", "npm test"))


# ── Composite build_challenges (preserves orchestrator interface) ────────────

def build_challenges(
    challenge_descriptions: list[str],
    topic: str = "",
    instructor_notes: str = "",
    revision_feedback: str = "",
    prior_files: dict[str, dict[str, str]] | None = None,
    amend_instructions: str = "",
) -> BuildResult:
    """
    Generate challenge repos from a list of challenge descriptions.

    Routes to the appropriate flow:
    - Normal build: plan → generate per repo → assemble BuildResult
    - Revision: skip planner, detect ecosystem from prior files, regenerate with feedback
    - Amendment: plan with existing repo context → selective regeneration

    The function signature is unchanged for orchestrator compatibility.
    """
    if revision_feedback and prior_files:
        return _build_revision(
            challenge_descriptions, topic, instructor_notes,
            revision_feedback, prior_files,
        )

    if amend_instructions:
        return _build_amendment(
            challenge_descriptions, topic, instructor_notes,
            amend_instructions, prior_files,
        )

    # Normal flow: plan then generate per repo
    plan = plan_challenges(challenge_descriptions, topic, instructor_notes)

    repos = []
    for spec in plan.repos:
        repo = generate_repo(spec, topic, instructor_notes)
        repos.append(repo)

    # Save challenge type notes to knowledge base
    notes = plan.challenge_type_notes.strip()
    if notes and notes not in ("", "N/A", "None"):
        _save_challenge_type_notes(topic or challenge_descriptions[0], notes)

    return BuildResult(repos=repos)


def _build_revision(
    challenge_descriptions: list[str],
    topic: str,
    instructor_notes: str,
    revision_feedback: str,
    prior_files: dict[str, dict[str, str]],
) -> BuildResult:
    """Re-generate repos using targeted feedback (skips planner and reference docs)."""
    repos = []

    for repo_name, files in prior_files.items():
        ecosystem = _detect_ecosystem(files)
        is_ui = _detect_is_ui(ecosystem)
        install_cmd, test_cmd = _infer_commands(ecosystem)

        # Identify creative files (non-boilerplate) from prior output
        boilerplate_paths = set(get_boilerplate(ecosystem).keys())
        creative_files = [p for p in files if p not in boilerplate_paths]

        spec = RepoSpec(
            name=repo_name,
            description=challenge_descriptions[0] if challenge_descriptions else "",
            challenges=challenge_descriptions,
            ecosystem=ecosystem,
            install_command=install_cmd,
            test_command=test_cmd,
            creative_files=creative_files,
            is_ui=is_ui,
        )

        # Only send creative files as prior context (skip boilerplate)
        creative_prior = {p: c for p, c in files.items() if p not in boilerplate_paths}

        # Use _revise_repo: skips reference docs, outputs only changed files
        repo = _revise_repo(
            spec, topic, instructor_notes,
            revision_feedback=revision_feedback,
            prior_creative=creative_prior,
        )
        repos.append(repo)

    return BuildResult(repos=repos)


def _build_amendment(
    challenge_descriptions: list[str],
    topic: str,
    instructor_notes: str,
    amend_instructions: str,
    prior_files: dict[str, dict[str, str]] | None,
) -> BuildResult:
    """Amendment mode: plan the amendment scope, then selectively regenerate."""
    # Build existing repo metadata for the planner
    existing_repos = None
    if prior_files:
        existing_repos = []
        for name, files in prior_files.items():
            eco = _detect_ecosystem(files)
            existing_repos.append({"name": name, "ecosystem": eco, "challenges": []})

    # Include amendment instructions in the notes for the planner
    combined_notes = amend_instructions
    if instructor_notes:
        combined_notes = f"{amend_instructions}\n\n{instructor_notes}"

    plan = plan_challenges(
        challenge_descriptions, topic,
        instructor_notes=combined_notes,
        existing_repos=existing_repos,
    )

    repos = []
    for spec in plan.repos:
        prior = prior_files.get(spec.name) if prior_files else None

        if prior:
            # Existing repo — regenerate with amendment context
            boilerplate_paths = set(get_boilerplate(spec.ecosystem).keys())
            creative_prior = {p: c for p, c in prior.items() if p not in boilerplate_paths}

            amendment_feedback = (
                "## Amendment Mode\n\n"
                "You are amending an existing repo. Apply these changes:\n\n"
                f"{amend_instructions}\n\n"
                "Return ALL creative files with COMPLETE contents — "
                "not just the changed ones. You may add new files if needed."
            )

            repo = generate_repo(
                spec, topic, instructor_notes,
                revision_feedback=amendment_feedback,
                prior_files=creative_prior,
            )
        else:
            # New repo — generate from scratch
            repo = generate_repo(spec, topic, instructor_notes)

        repos.append(repo)

    # Save challenge type notes
    notes = plan.challenge_type_notes.strip()
    if notes and notes not in ("", "N/A", "None"):
        _save_challenge_type_notes(topic or challenge_descriptions[0], notes)

    return BuildResult(repos=repos)


