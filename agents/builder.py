# ── Reference doc filtering ──────────────────────────────────────────────────

# Mapping: ecosystem -> keywords to match against section headers in
# generation-patterns.md.  Matching is word-boundary-aware to prevent
# "Java" from matching "JavaScript".
_PATTERN_KEYWORDS: dict[str, list[str]] = {
    "typescript": ["TypeScript", "JavaScript", "JS Test"],
    "react":      ["TypeScript", "JavaScript", "React", "JS Test"],
    "vue":        [],
    "angular":    ["Angular"],
    "python":     ["Python"],
    "java":       ["Java"],
    "cpp":        ["C++"],
    "cobol":      ["COBOL"],
}

# Keywords for project-templates.md section matching
_TEMPLATE_KEYWORDS: dict[str, list[str]] = {
    "typescript": ["JavaScript / TypeScript"],
    "react":      ["React"],
    "vue":        ["Vue"],
    "angular":    ["Angular"],
    "python":     ["Python"],
    "java":       ["Java"],
    "cpp":        ["C++"],
    "cobol":      ["COBOL"],
}

# Always included regardless of ecosystem
_ALWAYS_INCLUDE = ["Universal", "Decision"]
_ALWAYS_INCLUDE_TEMPLATES = [".gitignore"]


def _split_sections(text: str) -> list[tuple[str, str]]:
    """Split markdown text by ## headings. Returns [(header, content), ...]."""
    sections: list[tuple[str, str]] = []
    current_header = ""
    current_lines: list[str] = []

    for line in text.splitlines(keepends=True):
        if line.startswith("## "):
            if current_header or current_lines:
                sections.append((current_header, "".join(current_lines)))
            current_header = line.strip().lstrip("#").strip()
            current_lines = [line]
        else:
            current_lines.append(line)

    if current_header or current_lines:
        sections.append((current_header, "".join(current_lines)))

    return sections


def _keyword_matches_header(keyword: str, header: str) -> bool:
    """Check if keyword matches in header with word-boundary awareness.

    Prevents 'Java' from matching 'JavaScript' by checking that the
    character after the match (if any) is not alphabetic.
    """
    h = header.lower()
    k = keyword.lower()
    idx = h.find(k)
    if idx == -1:
        return False
    end = idx + len(k)
    if end < len(h) and h[end].isalpha():
        return False
    return True


def _filter_sections(
    sections: list[tuple[str, str]],
    keywords: list[str],
    always_include: list[str],
) -> str:
    """Return content from sections whose headers match any keyword."""
    parts = []
    for header, content in sections:
        if not header:
            continue
        for kw in always_include + keywords:
            if _keyword_matches_header(kw, header):
                parts.append(content)
                break
    return "\n".join(parts)


def _load_filtered_reference_docs(ecosystem: str, is_ui: bool) -> str:
    """Load reference docs filtered to a specific ecosystem."""
    patterns_text = read_file(config.GENERATION_PATTERNS)
    templates_text = read_file(config.PROJECT_TEMPLATES)

    patterns_sections = _split_sections(patterns_text)
    templates_sections = _split_sections(templates_text)

    pattern_kws = _PATTERN_KEYWORDS.get(ecosystem, [])
    template_kws = _TEMPLATE_KEYWORDS.get(ecosystem, [])

    filtered_patterns = _filter_sections(
        patterns_sections, pattern_kws, _ALWAYS_INCLUDE)
    filtered_templates = _filter_sections(
        templates_sections, template_kws, _ALWAYS_INCLUDE_TEMPLATES)

    parts = [
        f"## Generation Patterns\n\n{filtered_patterns}",
        f"## Project Templates\n\n{filtered_templates}",
    ]

    if is_ui:
        ui_styling = read_file(config.UI_STYLING)
        parts.append(f"## UI Styling Guide\n\n{ui_styling}")

    return "\n\n".join(parts)


def _load_reference_docs() -> str:
    """Load all reference docs unfiltered (fallback)."""
    patterns = read_file(config.GENERATION_PATTERNS)
    templates = read_file(config.PROJECT_TEMPLATES)
    ui_styling = read_file(config.UI_STYLING)
    return (
        f"## Generation Patterns\n\n{patterns}\n\n"
        f"## Project Templates\n\n{templates}\n\n"
        f"## UI Styling Guide\n\n{ui_styling}"
    )


# ── Knowledge base loading ───────────────────────────────────────────────────

def _load_knowledge_base(topic: str) -> str:
    """Load any relevant knowledge base entries for this topic."""
    kb_parts = []

    # Load lessons learned
    lessons = read_file(config.LESSONS_LEARNED)
    if lessons.strip() and "Builder will append" not in lessons:
        kb_parts.append(f"## General Lessons Learned\n\n{lessons}")

    # Load any challenge type files
    if config.CHALLENGE_TYPES_DIR.exists():
        for type_file in sorted(config.CHALLENGE_TYPES_DIR.glob("*.md")):
            content = type_file.read_text()
            kb_parts.append(f"## Knowledge Base: {type_file.stem}\n\n{content}")

    return "\n\n".join(kb_parts)


